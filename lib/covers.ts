/**
 * lib/covers.ts
 *
 * Registry of preset cover images shipped in /public/covers/. Each cover
 * has an id, a display name, and a SHA-256 hash that the runtime
 * verifies before embedding. If the file on disk has been swapped or
 * corrupted, embedding refuses to proceed.
 *
 * To regenerate hashes: pnpm covers:generate && pnpm covers:hash
 * Then paste the output of covers:hash into COVER_HASHES below.
 */

