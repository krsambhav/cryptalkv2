export function getCover(id: string): Cover | undefined {
  return COVERS.find((c) => c.id === id);
}

/** Picks a random cover id from a pool, or from all covers if none provided. */
export function pickRandomCoverId(pool?: ReadonlyArray<string>): string {
  const ids = pool && pool.length > 0 ? pool : COVERS.map((c) => c.id);
  const idx = Math.floor(Math.random() * ids.length);
  return ids[idx]!;
}

/**
 * Fetches a cover and verifies its SHA-256 hash matches our build-time
 * record. Returns the verified bytes. Throws if the hash doesn't match.
 *
 * This is a tamper-detection layer, not a security boundary — a hostile
 * actor in a position to swap files in /public/ could also swap the
 * code that does the check. The point is to fail loudly when an
 * accidental file replacement happens (e.g., during deployment).
 */
export async function fetchAndVerifyCover(id: string): Promise<Blob> {
  const cover = getCover(id);
  if (!cover) throw new Error(`unknown cover: ${id}`);
  const expectedHash = COVER_HASHES[id];
  if (!expectedHash) throw new Error(`no hash for cover: ${id}`);

  const res = await fetch(cover.url, { cache: "force-cache" });
  if (!res.ok) throw new Error(`fetch ${cover.url} failed: ${res.status}`);
  const buf = new Uint8Array(await res.arrayBuffer());
  const actual = await sha256(buf);
  if (actual !== expectedHash) {
    throw new Error(
      `cover hash mismatch for ${id}: expected ${expectedHash.slice(0, 12)}…, got ${actual.slice(0, 12)}…`,
    );
  }
  return new Blob([buf as BlobPart], { type: "image/png" });
}
